package Business.PharmaceuticalCompany;

/**
 *
 * @author rekhajindal
 */
public class DrugGuidelines {
    
    private String drugInformation;
    private String ingredients;
    private String sideEffects;
    private String instructionForUse;
    private String dosage;
    private String drugStorageInfo;

    public String getDrugInformation() {
        return drugInformation;
    }

    public void setDrugInformation(String drugInformation) {
        this.drugInformation = drugInformation;
    }

    public String getIngredients() {
        return ingredients;
    }

    public void setIngredients(String ingredients) {
        this.ingredients = ingredients;
    }

    public String getSideEffects() {
        return sideEffects;
    }

    public void setSideEffects(String sideEffects) {
        this.sideEffects = sideEffects;
    }

    public String getInstructionForUse() {
        return instructionForUse;
    }

    public void setInstructionForUse(String instructionForUse) {
        this.instructionForUse = instructionForUse;
    }

    public String getDosage() {
        return dosage;
    }

    public void setDosage(String dosage) {
        this.dosage = dosage;
    }

    public String getDrugStorageInfo() {
        return drugStorageInfo;
    }

    public void setDrugStorageInfo(String drugStorageInfo) {
        this.drugStorageInfo = drugStorageInfo;
    }

    
    
    
}
