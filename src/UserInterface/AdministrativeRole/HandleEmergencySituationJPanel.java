    private void btnAssignDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignDoctorActionPerformed
        // TODO add your handling code here:
        try
        {
            Pattern pattern;
            pattern = Pattern.compile("[a-zA-Z ]+");
        long  patientMRN=Long.parseLong(txtpatientMRN.getText());
        String patientName=txtPatientName.getText();
        String message=txtMessage.getText();
        
        if(patientName.isEmpty() || message.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please enter proper value");
            return;
        }
        
        Matcher finame = pattern.matcher(patientName);
         if(!finame.matches())
         {
             JOptionPane.showMessageDialog(null,"Only alphabhats are allowed");
             return;
         }
        int selectedRow=DoctorDetailsJTable.getSelectedRow();
        if(selectedRow <0)
        {
            JOptionPane.showMessageDialog(null,"please select a row");
            return;
        }
        
        Doctor doctor=(Doctor)DoctorDetailsJTable.getValueAt(selectedRow, 0);
        EmergencyAssignWorkRequest request=new EmergencyAssignWorkRequest();
//        request.setDoctor(doctor);
        request.setPatientMRN(patientMRN);
        request.setPatientName(patientName);
        request.setMessage(message);
        request.setSenderEnterprise(enterprise);
        doctor.getWorkQueue().getWorkRequestList().add(request);
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "please enter proper number");
        }
    }//GEN-LAST:event_btnAssignDoctorActionPerformed

