    public void populateDoctorTable()
    {
        DefaultTableModel model=(DefaultTableModel)DoctorDetailsJTable.getModel();
        model.setRowCount(0);
        for(Organization org:enterprise.getOrganizationDirectory().getOrganizationList())
        {
            if(org instanceof DoctorOrganization)
            {
                for(Person person:org.getPerosnDirectory().getPersonList())
                {
                    Doctor doctor=(Doctor)person;
                    Object[]row=new Object[3];
                    row[0]=doctor;
                    row[1]=doctor.getFirstName();
                    row[2]=doctor.getSpecialization();
                    model.addRow(row);
                }
            }
        }
    }
