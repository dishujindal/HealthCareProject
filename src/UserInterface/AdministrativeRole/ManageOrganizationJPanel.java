    public void populateOrganizationCombo() {
        comboOrganizationType.removeAllItems();

        if (enterprise instanceof HospitalEnterprise) 
        {
            for (Type type : Organization.Type.values()) {
                if (type.getValue().equals(Type.Doctor.getValue()) || type.getValue().equals(Type.LabAssistant.getValue()) || type.getValue().equals(Type.Patient.getValue()) )
                {
                    comboOrganizationType.addItem(type);
                }
            }
        }
         else if (enterprise instanceof ClinicEnterprise) 
          {
            for (Type type : Organization.Type.values()) 
            {
                if (type.getValue().equals(Type.Doctor.getValue()) || type.getValue().equals(Type.LabAssistant.getValue())|| type.getValue().equals(Type.Patient.getValue()) )
                {
                    comboOrganizationType.addItem(type);
                }
            }
         }
        
        else if (enterprise instanceof PharmacyEnterprise) 
          {
            for (Type type : Organization.Type.values()) 
            {
                if (type.getValue().equals(Type.PharmacyStores.getValue())) 
                {
                    comboOrganizationType.addItem(type);
                }
            }
         }
        
        else if (enterprise instanceof PharmacyCompanyEnterprise) 
          {
            for (Type type : Organization.Type.values()) 
            {
                if (type.getValue().equals(Type.DrugManagement.getValue()))
                {
                    comboOrganizationType.addItem(type);
                }
            }
         }
        }

    

