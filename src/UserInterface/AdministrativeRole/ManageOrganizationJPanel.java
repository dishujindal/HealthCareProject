    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:
        Type type=(Type)comboOrganizationType.getSelectedItem();
        for(Organization org:enterprise.getOrganizationDirectory().getOrganizationList())
        {
            if(type.getValue().equalsIgnoreCase(org.getName()))
            {
                JOptionPane.showMessageDialog(null, "This organization already exists.");
                return;
            }
        }
        enterprise.getOrganizationDirectory().createOrganization(type);
        populateOrganizationTable();
        
    }//GEN-LAST:event_btnsubmitActionPerformed

