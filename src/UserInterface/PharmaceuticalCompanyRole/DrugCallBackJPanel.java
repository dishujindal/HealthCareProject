    public void populateDrugTable()
    {
        DefaultTableModel model=(DefaultTableModel)DrugDetailsTable.getModel();
        model.setRowCount(0);
        if(enterprise instanceof PharmacyCompanyEnterprise)
        {
            PharmacyCompanyEnterprise phEnterprise=(PharmacyCompanyEnterprise)enterprise;
                for(Drug drug:phEnterprise.getDrugList())
                {
                    if(drug.getDrugStatus().equalsIgnoreCase("Approved"))
                    {
                        Object[]row=new Object[3];
                        row[0]=drug;
                        row[1]=drug.getDrugName();
                        row[2]=drug.getDrugStatus();
                        model.addRow(row);
                    }
                }
    }
    }
    
    
