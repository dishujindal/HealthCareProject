    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        int selectedRow=DrugDetailsTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null, "please select a row");
            return;
        }
        Drug drug=(Drug)DrugDetailsTable.getValueAt(selectedRow, 0);
        drug.setDrugStatus("CallBack");
        String message=txtMessage.getText();
        if(message.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "please enter the message");
            return;
        }
        DrugCallBackRequestWorkRequest callBackRequest=new DrugCallBackRequestWorkRequest();
        callBackRequest.setDrug(drug);
        callBackRequest.setSenderEnterprise(enterprise);
        callBackRequest.setMessage(message);
        PharmacyCompanyEnterprise phComEnterprise=(PharmacyCompanyEnterprise)enterprise;
        for(Enterprise enterprise:phComEnterprise.getPharmacyList())
        {
            if(enterprise instanceof PharmacyEnterprise)
            {
                PharmacyEnterprise phEnterprise=(PharmacyEnterprise)enterprise;
                phEnterprise.getWorkQueue().getWorkRequestList().add(callBackRequest);
                
            }
        }
        phComEnterprise.getWorkQueue().getWorkRequestList().add(callBackRequest);
    }//GEN-LAST:event_btnRequestActionPerformed

