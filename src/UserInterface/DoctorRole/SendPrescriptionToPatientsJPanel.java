    public void populateComboDrug(PharmacyEnterprise phEnterprise)
    {
        combodrug.removeAllItems();
        for(Drug drug:phEnterprise.getPharmacyDrugList())
            {
                if(drug.getDrugStatus().equals("Approved"))
                {
                    combodrug.addItem(drug);
                }
                
            }
    }

