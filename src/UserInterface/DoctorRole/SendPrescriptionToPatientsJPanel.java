    private void btnSendPharmacyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendPharmacyActionPerformed
        // TODO add your handling code here:
        String messageToPharmacy=txtPharmacyMessage.getText();
        if(messageToPharmacy.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "please enter message");
        }
        Enterprise enterprise=(Enterprise)comboPharmacy.getSelectedItem();
        if(enterprise ==null)
        {
            JOptionPane.showMessageDialog(null,"please select pharmacy");
            return;
        }
        Drug drug=(Drug)combodrug.getSelectedItem();
        if(drug==null)
        {
            JOptionPane.showMessageDialog(null, "please select drug properly");
            return;
        }
        Doctor doctor=encounter.getDoctor();
        Patient patient=encounter.getPatient();
        
        PharmacyWorkRequest phrequest=new PharmacyWorkRequest();
        phrequest.setDoctor(doctor);
        phrequest.setPatient(patient);
        phrequest.setEnterprise(encounter.getLabWorkRequest().getSenderEnterprise());
        phrequest.setMessage(messageToPharmacy);
        phrequest.setStatus("send to pharmacy");
        phrequest.setDrugSender(enterprise.getName());
        phrequest.setSendDrug(drug);
        doctor.getWorkQueue().getWorkRequestList().add(phrequest);
        patient.getWorkQueue().getWorkRequestList().add(phrequest);
        enterprise.getWorkQueue().getWorkRequestList().add(phrequest);
        encounter.getPrescribedDrug().add(drug);
        
    }//GEN-LAST:event_btnSendPharmacyActionPerformed

