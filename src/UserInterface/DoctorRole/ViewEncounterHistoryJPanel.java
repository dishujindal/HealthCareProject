    private void btnViewEncounterListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewEncounterListActionPerformed
        // TODO add your handling code here:
        int selectedRow=encounterDetailsJTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null, "please select a row");
            return;
        }
        Encounter encounter=(Encounter)encounterDetailsJTable.getValueAt(selectedRow,0);
        ViewEncounterDetailsJPanel panel=new ViewEncounterDetailsJPanel(userProcessContainer,encounter);
        userProcessContainer.add("ViewEncounterDetails", panel);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnViewEncounterListActionPerformed

