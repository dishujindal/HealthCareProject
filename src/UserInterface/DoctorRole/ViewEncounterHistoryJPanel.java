    public void populateEncounterDetailsTable()
    {
        DefaultTableModel model=(DefaultTableModel)encounterDetailsJTable.getModel();
        model.setRowCount(0);
        for(Encounter encounter:encounterList)
        {
            Object[]row=new Object[2];
            row[0]=encounter;
            row[1]=encounter.getDate();
            model.addRow(row);
        }
    }
