    private void btnViewEncounterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewEncounterActionPerformed
        // TODO add your handling code here:
        int selectedRow=EncounterDetailsTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null, "please select a row");
        }
        Encounter encounter=(Encounter)EncounterDetailsTable.getValueAt(selectedRow,0);
        ViewEncounterDetailsJPanel panel=new ViewEncounterDetailsJPanel(userProcessContainer, encounter);
        userProcessContainer.add("ViewEncounterforEmergencyPanel", panel);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnViewEncounterActionPerformed

    public void populateEncounterDetails(EmergencyEnquiryWorkRequest request)
    {
        DefaultTableModel model=(DefaultTableModel)EncounterDetailsTable.getModel();
        model.setRowCount(0);
        for(Encounter encounter:request.getEncounterList())
        {
            Object[]row=new Object[2];
            row[0]=encounter;
            row[1]=encounter.getDate();
            model.addRow(row);
        }
        
    }
