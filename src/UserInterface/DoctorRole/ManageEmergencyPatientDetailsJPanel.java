    public void populatePateintDetailsTable()
    {
        DefaultTableModel model=(DefaultTableModel)EmergencyPatientDetailsTable.getModel();
        model.setRowCount(0);
        for(WorkRequest request:doctor.getWorkQueue().getWorkRequestList())
        {
            if(request instanceof EmergencyEnquiryWorkRequest)
            {
                EmergencyEnquiryWorkRequest enqRequest=(EmergencyEnquiryWorkRequest)request;
                Object[]row=new Object[3];
                row[0]=enqRequest;
                row[1]=enqRequest.getPatientMRN();
                row[2]=enqRequest.getSenderEnterprise();
                model.addRow(row);
            }
        }
    }
