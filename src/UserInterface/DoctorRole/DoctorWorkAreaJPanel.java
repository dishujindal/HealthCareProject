    public void populateVoidEmergencyWorkRequestTable()
    {
        DefaultTableModel model=(DefaultTableModel)emergencyWorkRequestTable.getModel();
        model.setRowCount(0);
        for(WorkRequest request:doctor.getWorkQueue().getWorkRequestList())
        {
            if(request instanceof EmergencyAssignWorkRequest)
            {
                EmergencyAssignWorkRequest emergencyRequest=(EmergencyAssignWorkRequest)request;
                Object[]row=new Object[4];
                row[0]=emergencyRequest;
                row[1]=emergencyRequest.getPatientMRN();
                row[2]=emergencyRequest.getPatientName();
                row[3]=emergencyRequest.getSenderEnterprise();
                model.addRow(row);
            }
        }
    }
