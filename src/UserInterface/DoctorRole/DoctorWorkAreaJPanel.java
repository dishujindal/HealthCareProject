    private void btnSendEnquiryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendEnquiryActionPerformed
        // TODO add your handling code here:
        int selectedRow=emergencyWorkRequestTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null, "please select a row");
            return;
        }
        WorkRequest request=(WorkRequest)emergencyWorkRequestTable.getValueAt(selectedRow,0);
        long patientMRN=(Long)emergencyWorkRequestTable.getValueAt(selectedRow, 1);
        Enterprise senderEnterprise=(Enterprise)emergencyWorkRequestTable.getValueAt(selectedRow,3);
        String message=txtmessage.getText();
        if(message.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "please enter the message");
        }
        EmergencyEnquiryWorkRequest enquiryRequest=new EmergencyEnquiryWorkRequest();
        enquiryRequest.setPatientMRN(patientMRN);
        enquiryRequest.setDoctor(doctor);
        enquiryRequest.setSenderEnterprise(senderEnterprise);
        enquiryRequest.setMessage(message);
        doctor.getWorkQueue().getWorkRequestList().add(enquiryRequest);
        for(Network network:system.getNetworkList())
        {
            for(NetworkState state:network.getNetworkStateList())
            {
                for(NetworkCity city:state.getNetworkCityList())
                {
                    for(Enterprise enterprise:city.getEnterpriseDirectory().getEnterpriseList())
                    {
                        if(enterprise instanceof HospitalEnterprise || enterprise instanceof ClinicEnterprise)
                        {
                            enterprise.getWorkQueue().getWorkRequestList().add(enquiryRequest);
                        }
                    }
                }
            }
        }
        doctor.getWorkQueue().getWorkRequestList().remove(request);
    }//GEN-LAST:event_btnSendEnquiryActionPerformed

