    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = EnterpriseJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select atleast one row");
            return;
        }
        NetworkCity city=(NetworkCity)EnterpriseJTable.getValueAt(selectedRow,4);
        Enterprise enterprise=(Enterprise)EnterpriseJTable.getValueAt(selectedRow, 0);
        city.getEnterpriseDirectory().getEnterpriseList().remove(enterprise);
    }//GEN-LAST:event_btnDeleteActionPerformed

