    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:
        
        if(comboCountry.getItemCount()==0)
        {
            JOptionPane.showMessageDialog(null,"please add country");
            return;
        }
        
        if(Combonetworkstate.getItemCount()==0)
        {
            JOptionPane.showMessageDialog(null,"please add state");
            return;
        }
        
        NetworkCity city=(NetworkCity)comboNetworkCity.getSelectedItem();
        Enterprise.EnterpriseType type=(Enterprise.EnterpriseType)comboEnterprisetype.getSelectedItem();
        
         if (city == null || type == null){
            JOptionPane.showMessageDialog(null, "please select proper city and enterprise");
            return;
        }
        String enterpriseName=txtEnterprisename.getText();
        if(enterpriseName.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"please add enterprisename");
            return;
        }
        for(Enterprise enterprise:city.getEnterpriseDirectory().getEnterpriseList())
        {
            if(enterprise.getName().equals(enterpriseName))
            {
                JOptionPane.showMessageDialog(null,"This enterprise is already present");
                return;
            }
        }
       Enterprise enterprise= city.getEnterpriseDirectory().createAndAddEnterprise(enterpriseName, type);
       populateEnterpriseTable();
       txtEnterprisename.setText("");
    }//GEN-LAST:event_btnsubmitActionPerformed

