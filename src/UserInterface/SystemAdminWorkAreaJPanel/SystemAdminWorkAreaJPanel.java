    private void btnManageEnterpriseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageEnterpriseActionPerformed
        // TODO add your handling code here:
        for(Network network:system.getNetworkList())
        {
            for(NetworkState state:network.getNetworkStateList())
            {
                for(NetworkCity city:state.getNetworkCityList())
                {
                    if(city!=null)
                    {
                        ManageEnterpriseJPanel panel= new ManageEnterpriseJPanel(userProcessContainer,system);
                        userProcessContainer.add("ManageEnterpriseJPanel", panel);
                        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
                        layout.next(userProcessContainer);
                     return;   
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(null,"please add a network");
        
        
    }//GEN-LAST:event_btnManageEnterpriseActionPerformed

    private void btnManageuserAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageuserAccountActionPerformed
        // TODO add your handling code here:
        for(Network network:system.getNetworkList())
        {
            for(NetworkState state:network.getNetworkStateList())
            {
                for(NetworkCity city:state.getNetworkCityList())
                {
                    for(Enterprise enterprise:city.getEnterpriseDirectory().getEnterpriseList())
                    {
                        if(enterprise !=null)
                        {
                        ManageUserAccountJPanel panel=new ManageUserAccountJPanel(userProcessContainer,system);
                        userProcessContainer.add("ManageUserAccountJPanel", panel);
                        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
                        layout.next(userProcessContainer);
                        return;
                        }
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(null, "please add enterprise in system");
        
    }//GEN-LAST:event_btnManageuserAccountActionPerformed

