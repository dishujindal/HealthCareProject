    private void btnSendmsgPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendmsgPatientActionPerformed
        // TODO add your handling code here:
        int selectedRow=RatingDetailsJtable.getSelectedRow();
        String message=enquiremsgtxtArea.getText();
        if(message.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "please enter the message");
            return;
        }
        DoctorRatings rating=(DoctorRatings)RatingDetailsJtable.getValueAt(selectedRow, 0);
        PatientToPatientWorkRequest request=new PatientToPatientWorkRequest();
        request.setMessage(message);
        request.setSender(patient);
        request.setReceiver(rating.getPatient());
        request.setDoctorName(doctor.getFirstName());
        patient.getWorkQueue().getWorkRequestList().add(request);
        rating.getPatient().getWorkQueue().getWorkRequestList().add(request);
    }//GEN-LAST:event_btnSendmsgPatientActionPerformed

