    public void populateRatingTable()
    {
        DefaultTableModel model=(DefaultTableModel)RatingDetailsJtable.getModel();
        model.setRowCount(0);
        for(DoctorRatings rating:doctor.getRatingList())
        {
            Object[]row=new Object[3];
            row[0]=rating;
            row[1]=doctor.getFirstName();
            row[2]=rating.getPatient().getFirstName();
            model.addRow(row);
        }
    }
