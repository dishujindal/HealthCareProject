    public void populateCompanyDetailsTable()
    {
        DefaultTableModel model=(DefaultTableModel)CompanyDetailsJTable.getModel();
        model.setRowCount(0);
        for(Network country:system.getNetworkList())
        {
            for(NetworkState state:country.getNetworkStateList())
            {
                for(NetworkCity city:state.getNetworkCityList())
                {
                    for(Enterprise enterprise:city.getEnterpriseDirectory().getEnterpriseList())
                    {
                        if(enterprise instanceof PharmacyCompanyEnterprise)
                        {
                            Object[]row=new Object[2];
                            row[0]=enterprise;
                            row[1]=enterprise.getOrganizationID();
                            model.addRow(row);
                        }
                    }
                }
            }
        }
    }
