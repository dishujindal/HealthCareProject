    private void btnSelectCompanyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectCompanyActionPerformed
        // TODO add your handling code here:
        int selectedRow=CompanyDetailsJTable.getSelectedRow();
        if(selectedRow <0)
        {
            JOptionPane.showMessageDialog(null, "Please select atleast one row");
            return;
        }
        
        Enterprise enterprise=(Enterprise)CompanyDetailsJTable.getValueAt(selectedRow,0);
        pcEnterprise=(PharmacyCompanyEnterprise)enterprise;
        populateDrugDetails(enterprise);
        
        
        
    }//GEN-LAST:event_btnSelectCompanyActionPerformed

    private void btnAddDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDrugActionPerformed
        // TODO add your handling code here:
        int selectedRow=DrugDetailsJTable.getSelectedRow();
        if(selectedRow <0)
        {
            JOptionPane.showMessageDialog(null, "Please select atleast one row");
            return;
        }
        Drug drug=(Drug)DrugDetailsJTable.getValueAt(selectedRow, 0);
        PharmacyEnterprise phEnterprise=(PharmacyEnterprise)enterprise;
        for(Drug listDrug:phEnterprise.getPharmacyDrugList())
        {
            if(drug.getDrugID()==listDrug.getDrugID())
            {
                JOptionPane.showMessageDialog(null, "This drug is already presenet in list");
                return;
            }
        }
        phEnterprise.getPharmacyDrugList().add(drug);
        for(PharmacyEnterprise enterprise: pcEnterprise.getPharmacyList())
        {
            if(enterprise.getOrganizationID()==phEnterprise.getOrganizationID())
            {
                
                return;
            }
         
        }
        pcEnterprise.getPharmacyList().add(phEnterprise);
        
    }//GEN-LAST:event_btnAddDrugActionPerformed

