    private void populateDrugCallBackTable()
    {
        DefaultTableModel model=(DefaultTableModel)CallBackRequestTable.getModel();
        model.setRowCount(0);
        for(WorkRequest request:enterprise.getWorkQueue().getWorkRequestList())
        {
            if(request instanceof DrugCallBackRequestWorkRequest)
            {
                DrugCallBackRequestWorkRequest drugCallRequest=(DrugCallBackRequestWorkRequest)request;
                Object[]row=new Object[4];
                row[0]=drugCallRequest;
                row[1]=drugCallRequest.getDrug();
                row[2]=drugCallRequest.getDrug().getDrugName();
                row[3]=drugCallRequest.getSenderEnterprise().getName();
                model.addRow(row);
            }
        }
    }
