    private void btnProcessRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessRequestActionPerformed
        // TODO add your handling code here:
        int selectedRow=CallBackRequestTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"please select a row");
            return;
        }
        Drug drug=(Drug)CallBackRequestTable.getValueAt(selectedRow,1);
        PharmacyEnterprise phEnterprise=(PharmacyEnterprise)enterprise;
        
        boolean result=false;
        for(Drug findDrug:phEnterprise.getPharmacyDrugList())
        {
            
            if(findDrug.getDrugID()==drug.getDrugID())
            {
                phEnterprise.getPharmacyDrugList().remove(drug);
                result=true;
                break;
            }
            
        }
        if(result==false)
        {
            JOptionPane.showMessageDialog(null, "Sorry u are not allowed to process");
            return;
        }
        
        
    }//GEN-LAST:event_btnProcessRequestActionPerformed

