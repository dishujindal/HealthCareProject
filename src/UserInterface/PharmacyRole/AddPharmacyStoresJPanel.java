    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String storeName=txtStoreName.getText();
        String streetAddress=txtAddress.getText();
        String city=txtCity.getText();
        String state=txtState.getText();
        String country=txtcountry.getText();
        if((storeName.isEmpty())|| (streetAddress.isEmpty()) || (city.isEmpty()) || (state.isEmpty()) 
                || (country.isEmpty()))
        {
            JOptionPane.showMessageDialog(null, "Please enter the values properly.");
            return;
        }
        PharmacyStore store=new PharmacyStore();
        Address address=new Address();
        address.setCity(city);
        address.setState(state);
        address.setCountry(country);
        address.setStreetAdress(streetAddress);
        store.setStoreName(storeName);
        store.setAddress(address);
        organization.getStoreList().add(store);
        txtAddress.setText(null);
        txtCity.setText(null);
        txtState.setText(null);
        txtcountry.setText(null);
        txtStoreName.setText(null);
                
        
    }//GEN-LAST:event_btnSubmitActionPerformed

