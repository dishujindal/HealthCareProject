    public void getOrganization()
    {
        for(Organization org:enterprise.getOrganizationDirectory().getOrganizationList())
        {
            if(org instanceof PharmacyStoresOrganization)
            {
                organization = (PharmacyStoresOrganization) org;
            }
        }
    }
    public void populateStoreDetailTable()
    {
        DefaultTableModel model=(DefaultTableModel)StoresTable.getModel();
        model.setRowCount(0);
        for(PharmacyStore store:organization.getStoreList())
        {
            Object[]row=new Object[5];
            row[0]=store;
            row[1]=store.getStoreName();
            row[2]=store.getAddress().getCity();
            row[3]=store.getAddress().getState();
            row[4]=store.getAddress().getCountry();
            model.addRow(row);
            
        }
    }
