    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow=DrugDetailsJTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"please select a row");
            return;
        }
        
        Drug drug=(Drug)DrugDetailsJTable.getValueAt(selectedRow, 0);
        PharmacyEnterprise pharEnterprise=(PharmacyEnterprise)enterprise;
        pharEnterprise.getPharmacyDrugList().remove(drug);
    }//GEN-LAST:event_btnDeleteActionPerformed


