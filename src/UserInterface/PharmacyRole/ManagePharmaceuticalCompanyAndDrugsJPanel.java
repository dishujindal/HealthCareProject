    public void populateDrugTable()
    {
        DefaultTableModel model=(DefaultTableModel)DrugDetailsJTable.getModel();
        model.setRowCount(0);
        PharmacyEnterprise pharEnterprise=(PharmacyEnterprise)enterprise;
        for(Drug drug: pharEnterprise.getPharmacyDrugList())
        {
            
            Object[]row=new Object[3];
            row[0]=drug;
            row[1]=drug.getDrugName();
            row[2]=drug.getPrice();
            model.addRow(row);
        }
    }
    
